export function ansiEscapeGraphicMode(...parameters: string[]): string {
  return `${ANSI_ESCAPE_CODE}[${parameters.join(';')}m`;
}

export function ansiEscapeGraphicModeString(input: string, ...parameters: string[]): string {
  return `${ansiEscapeGraphicMode(...parameters)}${input}${ANSI_ESCAPE_RESET}`;
}
